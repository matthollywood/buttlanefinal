    <strong>Q. How can I get rid of a gap?</strong>
      If a tooth is missing, or needs taking out, there are several ways to fill the gap that is left. In some cases it is important to try to replace missing teeth to balance the way your jaw bites. If you have several missing teeth, the remaining teeth are under more pressure. This can lead to broken fillings or even jaw problems.<br>
A partial denture is the simplest way to replace missing teeth. However, some people find dentures uncomfortable and eventually decide to look at alternatives, such as bridges or implants.<br>
<Strong>Q. What is a bridge?</strong>
  <p>    Bridges are ideal for people who don't like dentures and only have a few teeth missing. Bridges are usually made by putting a crown on the teeth on either side of the gap and attaching a false tooth in the middle.<br>
The bridge can't be removed. These bridges are usually made of precious metal bonded to porcelain. Sometimes other non-precious metals are used in the base to give it extra strength. Quite often bridges are not available on the NHS but we can offer them privately. They are usually a lot cheaper than Implants.<br>
For more information from The Oral health Foundation click <a href="https://www.dentalhealth.org/bridges-and-partial-dentures" target="_blank">HERE</a> </p>
