<p>
      <Strong>Q. What Are Dentures?</strong><br>
      Dentures are a set of false teeth that enable individuals to eat and smile with confidence.  You can have either a complete set of dentures or a partial set, depending on how many teeth are missing and how many need to be replaced.
       People also find that dentures improve their general facial appearance.  The lack of cheek and lip support isn't something that we notice until we start to lose our teeth, but dentures play a significant role in facial balance, enhancing our cheeks and giving extra definition to your face.
       <br>
       <br>

      <strong>Q. What does the procedure involve?</strong><br>
       A badly fitted denture can make life really difficult.  Therefore it is worth investing the time to ensure your dentist can do the best job possible.  It may require a number of visits (sometimes up to five), so that your dentist can accurately determine the natural shape of your bite, jawbone and teeth.  Depending on whether you are having an NHS or a Private denture will decide which types of teeth your dentist can choose from.<br>
