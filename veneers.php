      A veneer is a new surface to fit over the front of a tooth. They're available on the NHS if they're needed to improve the health of your mouth – but not just to improve the appearance of your teeth (cosmetic reasons).
  Veneers can be made of:
<ul>
    <li> porcelain</li>
    <li>composite materials (the materials that make white fillings)</li>
</ul>
  Porcelain veneers are more realistic looking and long-lasting but also much more expensive than composite veneers. Ask our team for information.
</br>
</br>
<strong>Q. What is a veneer?</strong>
Veneers are thin slices of porcelain. These are precisely made to fit over the visible surface of your front teeth, like a false fingernail fits over a nail.
</br>
</br>
<strong>Q. Why might I have a veneer?</strong>
  Veneers are an ideal way of treating discoloured or unsightly teeth, closing gaps between front teeth, or repairing chips and cracks.</br></br>
<strong>Q. How are veneers fitted?</strong>
  <p>    A small amount of enamel is removed from the tooth, usually the same thickness as the veneer will be. The dental team take an impression and send it to a dental technician, who makes the veneer in a laboratory. The veneer is then bonded to the tooth to form a strong and natural-looking repair.
  For information from the Oral Health Foundation click <a href="https://www.dentalhealth.org/veneers" target="_blank">HERE </a></p>


