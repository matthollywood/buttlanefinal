  Tooth whitening kits are an easy and effective way to whiten your teeth.
  The dentist will take impressions of your teeth and make custom whitening trays, similar to gum shields, that fit your teeth perfectly.  These are used in combination with a whitening gel that you apply at home.  A small amount of gel is squeezed into the trays and then worn for a few hours each day or overnight (your dentist will explain this and give you exact instructions on use).</br>
  </br>
  <strong>Q. What are the Benefits?</strong>

    Whiten your smile at your convenience in the comfort of your own home</br>
    They are more effective than shop-bought kits</br>
    With your dentist, you can discuss the colour change you want to achieve</br>
    Top-up kits are available for some extra brightness on special occasions</br>

  </br>
  <strong>  Q. How Long does it take?</strong>
   The treatment will take four to six weeks.  During that time you will need to make two to three visits to the dentist so that he can check that everything is progressing well.  At the first appointment, your dentist or Hygiene-therapist will take a single impression of your teeth so that he or she can make the whitening trays.  Once your your trays has been fitted you will need to continue the treatment at home.  This means applying the whitening gel approximately every two days (exact instructions on how to use your kit will be provided by your dentist/ therapist).</br>
  </br>


  <strong>  Q. What If I Have Sensitive Teeth?</strong>



     If you suffer sensitivity you must say before you start. There shouldn't be any reason why you can't have tooth whitening but we may provide special treatments to stop them getting more sensitive during whitening.  Please be aware that some other whitening methods can cause much discomfort during and after whitening has finished.</br>
  </br>


    <strong>Q. How Long Do The Effects Last?</strong>



    The effect of the whitening lasts for many years.  However, this will vary from person to person.  If you smoke, eat or drink products that stain your teeth then the effect is less likely to last as long.  Careful and regular cleaning of your teeth, which is an essential part of your oral hygiene, will also help ensure the effects last for longer.  If they dull a little you can purchase additional whitening gels to top up and maintain the colour but this is usually only after many months. </br>
  </br>


  <strong>Q. When Might Tooth Whitening Not Work?</strong>



    It is important to note that Tooth whitening only works on natural tooth. Occasionally you may want some fillings replacing in white get the maximum advantage of the look. Tooth whitening will not work on any types of 'false' teeth such as dentures, crowns or veneers.  If your dentures are stained or discoloured visit your dentist and ask them to be sent for cleaning.  Stained veneers, crowns or dentures may need replacing. Again ask your dentist.</br>

  </br>

  <strong>Q. How Can I Look After My Teeth After Tooth Whitening?</strong>



    You can help keep your teeth white by cutting down on the amount of food and drink you have that stain teeth.  Don't forget, stopping smoking can also prevent discolouring and staining.</br>

